_ROOM_LIMITS = {
    # (min_w, max_w, min_h, max_h)  all in metres
    "toilet":   (1.5, 2.5,  2.0, 3.2),
    "corridor": (0.9, 2.0,  0.5, 20.0),
    "pooja":    (1.2, 2.2,  1.2, 2.5),
    "living":   (3.0, 9.0,  3.0, 9.0),
    "bedroom":  (2.8, 6.5,  3.2, 7.5),
    "master":   (3.0, 7.0,  3.5, 8.5),
    "kitchen":  (2.4, 5.5,  2.4, 6.5),
    "dining":   (2.0, 6.0,  2.0, 6.5),
    "store":    (1.5, 3.5,  1.5, 4.0),
    "balcony":  (1.2, 4.0,  1.0, 3.5),
    "study":    (2.5, 5.5,  2.5, 6.0),
    "default":  (1.5, 10.0, 1.5, 10.0),
}

def _rtype(name):
    n = name.lower()
    if any(k in n for k in ("toilet", "bath", "wc")):      return "toilet"
    if any(k in n for k in ("corridor", "passage", "foyer", "lobby")): return "corridor"
    if any(k in n for k in ("pooja", "puja", "prayer", "mandir")):     return "pooja"
    if "master" in n:                                                   return "master"
    if any(k in n for k in ("bedroom", "bed room", "bed")):            return "bedroom"
    if any(k in n for k in ("living", "hall", "lounge", "drawing")):   return "living"
    if "kitchen" in n:                                                  return "kitchen"
    if "dining" in n:                                                   return "dining"
    if any(k in n for k in ("store", "utility", "laundry", "servant")): return "store"
    if any(k in n for k in ("balcony", "terrace", "verandah")):        return "balcony"
    if any(k in n for k in ("study", "office", "work")):               return "study"
    return "default"

def _find_cuts(rooms, pos_key, size_key):
    """Return sorted list of axis positions where no room spans across."""
    bounds = set()
    for r in rooms:
        bounds.add(round(r[pos_key], 4))
        bounds.add(round(r[pos_key] + r[size_key], 4))
    cuts = []
    for p in sorted(bounds):
        if not any(r[pos_key] + 0.005 < p < r[pos_key] + r[size_key] - 0.005 for r in rooms):
            cuts.append(p)
    return cuts

def _zone_limits_x(zone_rooms):
    """
    Return (min_w, max_w, hard_max) for an x-zone.
    hard_max=True when the tightest maximum comes from a hard-type room (toilet,
    corridor, pooja) — meaning this zone must never grow beyond that limit.
    """
    y_cuts = _find_cuts(zone_rooms, 'y', 'h')
    mn_z, mx_z = 0.5, 20.0
    hard_max = False
    for j in range(len(y_cuts) - 1):
        ys, ye = y_cuts[j], y_cuts[j + 1]
        sr = [r for r in zone_rooms
              if abs(r['y'] - ys) < 0.015 and abs(r['y'] + r['h'] - ye) < 0.015]
        if not sr:
            continue
        types = [_rtype(r['name']) for r in sr]
        mn_s = sum(_ROOM_LIMITS.get(t, _ROOM_LIMITS['default'])[0] for t in types)
        mx_s = sum(_ROOM_LIMITS.get(t, _ROOM_LIMITS['default'])[1] for t in types)
        if mx_s < mx_z:
            mx_z = mx_s
            hard_max = any(t in _HARD_TYPES for t in types)
        mn_z = max(mn_z, mn_s)
    # If hard max < soft min (conflict: toilet max < bedroom min in same zone),
    # the hard cap wins — lower the effective minimum.
    if hard_max and mn_z > mx_z:
        mn_z = max(0.5, mx_z - 0.1)
    return max(mn_z, 0.5), max(mx_z, mn_z + 0.05), hard_max


def _zone_limits_y(zone_rooms):
    """
    Return (min_h, max_h, hard_max) for a y-zone.
    hard_max=True when the tightest maximum comes from a hard-type room.
    """
    x_cuts = _find_cuts(zone_rooms, 'x', 'w')
    mn_z, mx_z = 0.5, 20.0
    hard_max = False
    for i in range(len(x_cuts) - 1):
        xs, xe = x_cuts[i], x_cuts[i + 1]
        sr = [r for r in zone_rooms
              if abs(r['x'] - xs) < 0.015 and abs(r['x'] + r['w'] - xe) < 0.015]
        if not sr:
            continue
        types = [_rtype(r['name']) for r in sr]
        mn_s = sum(_ROOM_LIMITS.get(t, _ROOM_LIMITS['default'])[2] for t in types)
        mx_s = sum(_ROOM_LIMITS.get(t, _ROOM_LIMITS['default'])[3] for t in types)
        if mx_s < mx_z:
            mx_z = mx_s
            hard_max = any(t in _HARD_TYPES for t in types)
        mn_z = max(mn_z, mn_s)
    if hard_max and mn_z > mx_z:
        mn_z = max(0.5, mx_z - 0.1)
    return max(mn_z, 0.5), max(mx_z, mn_z + 0.05), hard_max

_HARD_TYPES = {"toilet", "corridor", "pooja", "balcony"}


def _distribute(orig_sizes, limits, target, can_overflow=None):
    """
    Scale orig_sizes to sum to target while respecting per-zone (min,max) limits.
    can_overflow: optional set of zone indices whose max is a soft limit (bedroom,
    living room, etc.) and may be exceeded when hard zones exhaust their architectural
    max and there is still space left to fill.
    """
    if not orig_sizes:
        return []
    orig_total = sum(orig_sizes)
    if orig_total <= 0:
        return list(orig_sizes)
    sizes = [s * target / orig_total for s in orig_sizes]

    # If prescribed minimums can't all fit in target, scale them down proportionally
    # so rooms still fill the plot (prevents negative "remaining" in small plots).
    min_sum = sum(lo for lo, hi in limits)
    if min_sum > target and min_sum > 0:
        scale_mins = target / min_sum
        limits = [(lo * scale_mins, hi) for lo, hi in limits]

    # fixed[i]: None = free; 'lo' = clamped at minimum; 'hi' = clamped at maximum
    fixed = [None] * len(sizes)

    for _ in range(40):
        clamped_any = False
        for i, (lo, hi) in enumerate(limits):
            if fixed[i] is not None:
                continue
            if sizes[i] < lo - 0.001:
                sizes[i] = lo
                fixed[i] = 'lo'
                clamped_any = True
            elif sizes[i] > hi + 0.001:
                sizes[i] = hi
                fixed[i] = 'hi'
                clamped_any = True

        free_idx = [i for i, f in enumerate(fixed) if f is None]
        if not free_idx:
            break

        fixed_sum = sum(sizes[i] for i in range(len(sizes)) if fixed[i] is not None)
        remaining = target - fixed_sum
        free_orig = sum(orig_sizes[i] for i in free_idx)
        for i in free_idx:
            sizes[i] = (orig_sizes[i] / free_orig * remaining) if free_orig > 0 else remaining / len(free_idx)

        if not clamped_any:
            break  # free zones already in range — converged

    # Push any remaining deficit into soft (can_overflow) zones only.
    # Hard-max zones (toilet, corridor, pooja) must never exceed their limit.
    total = sum(sizes)
    if abs(total - target) > 0.001:
        ov_idx = list(can_overflow or [])
        if ov_idx:
            deficit = target - total
            ov_orig = sum(orig_sizes[i] for i in ov_idx)
            for i in ov_idx:
                sizes[i] += deficit * (orig_sizes[i] / ov_orig if ov_orig > 0 else 1 / len(ov_idx))
        else:
            sizes = [s * target / total for s in sizes]
    return sizes

def _scale_constrained(rooms, target_w, target_d, orig_w, orig_d):
    """Scale rooms with per-type constraints using zone-based layout."""
    M = 0.23  # outer half-wall margin
    inner_w = target_w - 2 * M
    inner_d = target_d - 2 * M
    orig_inner_w = orig_w - 2 * M
    orig_inner_d = orig_d - 2 * M

    # ── X zones ──────────────────────────────────────────────────────────
    x_cuts = _find_cuts(rooms, 'x', 'w')
    x_orig = [x_cuts[i + 1] - x_cuts[i] for i in range(len(x_cuts) - 1)]
    def _xzone_rooms(i):
        z0, z1 = x_cuts[i], x_cuts[i + 1]
        return [r for r in rooms if r['x'] >= z0 - 0.015 and r['x'] + r['w'] <= z1 + 0.015]

    x_lims_full = [_zone_limits_x(_xzone_rooms(i)) for i in range(len(x_cuts) - 1)]
    x_lims = [(mn, mx) for mn, mx, _ in x_lims_full]
    x_can_overflow = {i for i, (_, _, hm) in enumerate(x_lims_full) if not hm}
    x_new  = _distribute(x_orig, x_lims, inner_w, can_overflow=x_can_overflow)

    new_x_cuts = [M]
    for s in x_new:
        new_x_cuts.append(new_x_cuts[-1] + s)

    # ── Assign each room to an x-zone ────────────────────────────────────
    def find_zone(r, cuts, pos_k, size_k):
        r0, r1 = r[pos_k], r[pos_k] + r[size_k]
        # prefer zone that the room exactly spans
        for i in range(len(cuts) - 1):
            if abs(r0 - cuts[i]) < 0.015 and abs(r1 - cuts[i + 1]) < 0.015:
                return i
        # fall back: zone that contains centre
        mid = (r0 + r1) / 2
        for i in range(len(cuts) - 1):
            if cuts[i] <= mid <= cuts[i + 1]:
                return i
        return 0

    room_xi = [find_zone(r, x_cuts, 'x', 'w') for r in rooms]

    # ── Y zones per x-zone ───────────────────────────────────────────────
    result = []
    for xi in range(len(x_orig)):
        z_rooms_idx = [i for i, xi2 in enumerate(room_xi) if xi2 == xi]
        z_rooms = [rooms[i] for i in z_rooms_idx]

        y_cuts = _find_cuts(z_rooms, 'y', 'h')
        y_orig = [y_cuts[j + 1] - y_cuts[j] for j in range(len(y_cuts) - 1)]
        def _yzone_rooms(j):
            yj0, yj1 = y_cuts[j], y_cuts[j + 1]
            return [r for r in z_rooms if r['y'] >= yj0 - 0.015 and r['y'] + r['h'] <= yj1 + 0.015]

        y_lims_full = [_zone_limits_y(_yzone_rooms(j)) for j in range(len(y_cuts) - 1)]
        y_lims = [(mn, mx) for mn, mx, _ in y_lims_full]
        y_can_overflow = {j for j, (_, _, hm) in enumerate(y_lims_full) if not hm}
        # Scale proportionally: rooms in this x-zone may not span the full depth
        y_scale = inner_d / orig_inner_d if orig_inner_d > 0 else 1.0
        y_target = sum(y_orig) * y_scale
        y_new  = _distribute(y_orig, y_lims, y_target, can_overflow=y_can_overflow)

        # Preserve the y-offset of the first room's zone start (scaled)
        y_offset = M + (y_cuts[0] - M) * y_scale if y_cuts else M
        new_y_cuts = [y_offset]
        for s in y_new:
            new_y_cuts.append(new_y_cuts[-1] + s)

        x_ratio = x_new[xi] / x_orig[xi] if x_orig[xi] > 0 else 1.0

        for i in z_rooms_idx:
            r = rooms[i]
            yi = find_zone(r, y_cuts, 'y', 'h')
            y_ratio = y_new[yi] / y_orig[yi] if y_orig[yi] > 0 else 1.0

            new_x = new_x_cuts[xi] + (r['x'] - x_cuts[xi]) * x_ratio
            new_y = new_y_cuts[yi] + (r['y'] - y_cuts[yi]) * y_ratio
            new_w = r['w'] * x_ratio
            new_h = r['h'] * y_ratio

            result.append((i, {
                **r,
                'x': round(new_x, 3),
                'y': round(new_y, 3),
                'w': round(new_w, 3),
                'h': round(new_h, 3),
            }))

    result.sort(key=lambda t: t[0])
    return [r for _, r in result]


