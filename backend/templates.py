def get_plan(bhk_type, plot_w_ft, plot_d_ft, style="modern"):
  import random
  FT = 0.3048
  plot_w = round(plot_w_ft * FT, 2)
  plot_d = round(plot_d_ft * FT, 2)

  bhk_key = bhk_type.upper().replace(" ","")
  matching = {k:v for k,v in PLANS.items() 
              if k.startswith(bhk_key)}
  if not matching:
    matching = PLANS

  def size_diff(plan):
    return abs(plan["plot_w"]-plot_w) + abs(plan["plot_d"]-plot_d)

  sorted_plans = sorted(matching.items(),
                        key=lambda x: size_diff(x[1]))
  top3 = sorted_plans[:3]
  chosen_key, chosen_plan = random.choice(top3)

  sx = (plot_w-0.46)/(chosen_plan["plot_w"]-0.46)
  sy = (plot_d-0.46)/(chosen_plan["plot_d"]-0.46)

  scaled_rooms = []
  for r in chosen_plan["rooms"]:
    name_l = r["name"].lower()
    w = round(r["w"]*sx, 3)
    h = round(r["h"]*sy, 3)
    if "toilet" in name_l:
      w = min(w, 2.2)
      h = min(h, 2.8)
    scaled_rooms.append({
      "name": r["name"],
      "x": round(0.23+(r["x"]-0.23)*sx, 3),
      "y": round(0.23+(r["y"]-0.23)*sy, 3),
      "w": w, "h": h,
      "door": r["door"],
      "window": r["window"],
      "x_px": 0, "y_px": 0,
      "w_px": 0, "h_px": 0,
    })

  return {
    "plot_w_m": plot_w,
    "plot_d_m": plot_d,
    "plot_w_ft": plot_w_ft,
    "plot_d_ft": plot_d_ft,
    "bhk_type": bhk_type,
    "style": style,
    "rooms": scaled_rooms,
    "room_count": len(scaled_rooms),
    "template_used": chosen_key,
    "compliance": {
      "overall": 0,
      "grade": "-",
      "summary": "Vastu scoring coming next"
    }
  }
